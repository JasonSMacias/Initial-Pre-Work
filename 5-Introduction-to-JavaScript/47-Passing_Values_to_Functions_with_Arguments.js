// In this lesson I learned how to pass arguments to functions and how parameters serve as placeholders for arguments to be passed

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

